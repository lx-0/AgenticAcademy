type Provider = "google" | "github" | "linkedin";

export function OAuthButtons({ providers }: { providers: Provider[] }) {
  const [isPending, startTransition] = useTransition();

  function handleOAuth(provider: Provider) {
    startTransition(async () => {
      await oauthSignInAction(provider);
    });
  }

  return (
    <div className="space-y-2.5">
      {providers.map((provider) => (
        <button
          key={provider}
          type="button"
          disabled={isPending}
          onClick={() => handleOAuth(provider)}
          className="w-full flex items-center justify-center gap-2.5 px-4 py-2.5 border border-gray-300 rounded-lg bg-white text-sm font-medium text-gray-700 hover:bg-gray-50 disabled:opacity-50 disabled:cursor-not-allowed transition-colors"
        >
          {PROVIDER_ICONS[provider]}
          Continue with {PROVIDER_LABELS[provider]}
        </button>
      ))}
    </div>
  );
}
